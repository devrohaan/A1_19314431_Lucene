public class CranfieldQuery {

    private String id;
    private String query;

    public CranfieldQuery() {
        this.id = "";
        this.query = "";
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getQuery() {
        return query;
    }

    public void setQuery(String query) {
        this.query = query;
    }
}
